#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	unsigned int	i;
	unsigned int	len;
	char			*s2;

	if (!s || !f)
		return (0);
	i = 0;
	len = ft_strlen(s);
	s2 = (char *)malloc(len + 1);
	if (s2 == 0)
		return (0);
	while (s[i])
	{
		s2[i] = f(i, s[i]);
		i++;
	}
	s2[i] = '\0';
	return (s2);
}
